				<h2>Payments</h2>
				
				<p>On selecting the Pay Monthly membership option, members will be required to pay the associated registration 					fee and the 1st month membership fee in advance. Members are required to agree to the normal terms and 					conditions of payment.</p>

				<p>This is a recurring monthly charge. Membership Fees will be debited from the members account, using their 					given bank debit/credit card, on or around their join date each month, for the minimum 1 month term from the 					term start date. Members can cancel their agreement and stop payments at any time, by simply logging in to 					their Member Area online and pressing the "Cancel" button.</p>
				
				<P>Members are required to keep their account in funds to meet these payments.  In the event of the failure to 					pay the monthly membership fee, missed payments will be re-submitted for payment by Goldstone Fitness Limited. 					Goldstone Fitness Limited reserves the right to cancel membership if payment is not received. Access to the 					facility will be denied until said payment is received and if payment is not received, and cancellation occurs, 				the registration fee becomes applicable again on re-joining.</p>

				<p>These arrears of fees must be paid by Credit or Debit card online using the Member Area.</p>
				
				<p>Failure to meet direct debit fee – In the event of the failure to pay the monthly direct debit fee you will 					firstly be notified that your membership has been temporarily stopped until the balance is paid.</p>

